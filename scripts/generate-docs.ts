/**
 * PND onchain-protocols reference-docs generator.
 *
 * Merges three inputs into an API-style reference:
 *   1. Checked-in ABIs            packages/abi/src/*.ts  (via @pin/abi)
 *   2. Mainnet deployments        contracts/deployments.mainnet.json (addresses)
 *   3. Hand-authored prose        docs/reference/_prose/<Name>.md
 *      + hand-written pages       docs/reference/_pages/**
 *
 * The reference is organized into GROUPS. Two are shared (Introduction,
 * Off-chain); the rest are protocols (Collections, Auctions, and more as they
 * ship). Each protocol is a self-contained doc set: an overview, concept pages,
 * per-contract references, guides, and its own cross-cutting indexes (access
 * control, errors, events). URLs are namespaced by group:
 *   /docs/introduction/overview
 *   /docs/collections/contracts/sovereign-collection
 *   /docs/auctions/contracts/sovereign-auction-house
 *
 * Outputs:
 *   docs/reference/<group>/<section?>/<slug>.md   final pages (GENERATED)
 *   apps/web/src/lib/docs/manifest.json           grouped sidebar tree + metadata
 *   apps/web/src/lib/docs/content.json            pre-rendered highlighted HTML
 *   apps/web/public/abis/<Name>.json              ABI copies for integrators/agents
 *   apps/web/public/protocol-manifest.json        machine-readable entry point
 *   apps/web/public/llms.txt                      LLM orientation file
 *   apps/web/public/docs-search-index.json        client-side search corpus
 *
 * The generator is deliberately strict: every ABI function/event/error must
 * have a prose block, every state-changing function must declare its access
 * model, and prose blocks naming unknown ABI items fail the build. Signatures
 * can never drift because they are derived, not written.
 *
 * The contracts are pre-deploy: unresolved {{addr:*}} tokens render as a
 * `<SCREAMING_SNAKE_ADDRESS>` placeholder (with a build-time notice) rather
 * than failing the build. Fill contracts/deployments.mainnet.json at launch.
 *
 * Prose format: see docs/reference/_prose/SPEC.md.
 */
