// ── Anchors (must match the app renderer's slugify) ────────────────────

function anchor(heading: string): string {
    return heading
        .toLowerCase()
        .replace(/[^a-z0-9\s-]/g, '')
        .trim()
        .replace(/\s+/g, '-');
}

// ── Markdown → HTML (pre-rendered at generation time) ──────────────────

interface TocEntry {
    depth: number;
    text: string;
    id: string;
}

interface RenderedPage {
    group: string;
    section: string;
    slug: string;
    title: string;
    description: string;
    html: string;
    toc: TocEntry[];
}

function escapeHtml(s: string): string {
    return s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');
}

function renderPageHtml(hl: Highlighter, page: BuiltPage): RenderedPage {
    const toc: TocEntry[] = [];
    const usedIds = new Map<string, number>();
    const loadedLangs = new Set(hl.getLoadedLanguages());
    const marked = new Marked({gfm: true});
    marked.use({
        renderer: {
            heading({tokens, depth, text}) {
                const inline = this.parser.parseInline(tokens);
                const base = anchor(text);
                const seen = usedIds.get(base) ?? 0;
                usedIds.set(base, seen + 1);
                const id = seen === 0 ? base : `${base}-${seen}`;
                if (depth === 2 || depth === 3) toc.push({depth, text, id});
                return `<h${depth} id="${id}"><a class="doc-anchor" href="#${id}">${inline}</a></h${depth}>\n`;
            },
            image({href, text}) {
                const m = href.match(/^\.\.\/_assets\/([\w-]+\.svg)$/);
                if (!m) return `<img src="${href}" alt="${escapeHtml(text)}" />`;
                const assetPath = join(ASSETS_DIR, m[1]);
                if (!existsSync(assetPath)) throw new Error(`missing diagram asset: ${assetPath}`);
                let svg = readFileSync(assetPath, 'utf8').replace(/<\?xml[^>]*\?>\s*/, '');
                const vb = svg.match(/viewBox="0 0 (\d+(?:\.\d+)?) (\d+(?:\.\d+)?)"/);
                if (!vb) throw new Error(`${assetPath}: svg needs a "0 0 w h" viewBox`);
                const maxW = Math.round(parseFloat(vb[1]) * 1.1);
                svg = svg.replace('<svg ', `<svg aria-label="${escapeHtml(text)}" `);
                return `<figure class="doc-figure" style="max-width:${maxW}px">${svg}</figure>\n`;
            },
            code({text, lang}) {
                const language = lang && loadedLangs.has(lang) ? lang : null;
                const body = language
                    ? hl.codeToHtml(text, {
                          lang: language,
                          themes: {light: 'github-light', dark: 'github-dark'},
                          defaultColor: 'light',
                      })
                    : `<pre class="shiki"><code>${escapeHtml(text)}</code></pre>`;
                const langTag = lang ? `<span class="doc-code-lang">${escapeHtml(lang)}</span>` : '';
                return `<div class="doc-code">${langTag}<button class="doc-copy" type="button" aria-label="Copy code">copy</button>${body}</div>\n`;
            },
        },
    });
    const html = marked.parse(page.markdown, {async: false}) as string;
    return {
        group: page.group,
        section: page.section,
        slug: page.slug,
        title: page.title,
        description: page.description,
        html,
        toc,
    };
}

// ── Search index ───────────────────────────────────────────────────────

interface SearchEntry {
    path: string;
    page: string;
    heading: string;
    anchor: string;
    text: string;
}

function stripMarkdown(md: string): string {
    return md
        .replace(/```[\s\S]*?```/g, ' ')
        .replace(/`([^`]*)`/g, '$1')
        .replace(/\[([^\]]*)\]\([^)]*\)/g, '$1')
        .replace(/^#+ /gm, '')
        .replace(/[*_>|]/g, ' ')
        .replace(/\s+/g, ' ')
        .trim();
}

function indexPage(page: BuiltPage): SearchEntry[] {
    const entries: SearchEntry[] = [];
    const segments = page.markdown.split(/^(?=#{1,3} )/m);
    for (const seg of segments) {
        const h = seg.match(/^(#{1,3}) (.+)\n/);
        const heading = h ? h[2].trim() : page.title;
        const body = stripMarkdown(h ? seg.slice(h[0].length) : seg);
        if (!body && !h) continue;
        entries.push({
            path: page.path,
            page: page.title,
            heading,
            anchor: h && h[1].length > 1 ? anchor(heading) : '',
            text: body.slice(0, 300),
        });
    }
    return entries;
}

// ── Main ───────────────────────────────────────────────────────────────

async function main() {
    const pages: BuiltPage[] = [];

    // Introduction (shared): overview, addresses (generated), conventions.
    pages.push(buildHandPage('introduction', '', 'overview', 'introduction/overview.md'));
    pages.push(buildAddressesPage());
    pages.push(buildHandPage('introduction', '', 'conventions', 'introduction/conventions.md'));

    // Each protocol: overview, concepts, contracts, guides, then reference indexes.
    for (const proto of PROTOCOLS) {
        pages.push(buildHandPage(proto.id, '', 'overview', proto.overview));
        for (const c of proto.concepts) pages.push(buildHandPage(proto.id, c.section, c.slug, c.file));
        for (const cp of proto.contracts) pages.push(buildContractPage(proto, cp));
        for (const g of proto.guides) pages.push(buildHandPage(proto.id, g.section, g.slug, g.file));
        pages.push(buildAccessControlPage(proto));
        pages.push(buildErrorsPage(proto));
        pages.push(buildEventsPage(proto));
        for (const rp of proto.referencePages) pages.push(buildHandPage(proto.id, rp.section, rp.slug, rp.file));
    }

    // Off-chain (shared).
    for (const p of SHARED_OFFCHAIN_PAGES) pages.push(buildHandPage('offchain', '', p.slug, p.file));

    // Write final markdown tree (wipe generated group dirs, keep _prose/_pages/_assets).
    for (const e of readdirSync(OUT_DIR, {withFileTypes: true})) {
        if (e.isDirectory() && !e.name.startsWith('_')) rmSync(join(OUT_DIR, e.name), {recursive: true, force: true});
    }
    for (const p of pages) {
        const rel = p.path.slice('/docs/'.length);
        const outFile = join(OUT_DIR, `${rel}.md`);
        mkdirSync(dirname(outFile), {recursive: true});
        const isContractRef = p.section === 'contracts';
        const banner = GENERATED_BANNER(isContractRef ? `_prose/ + @pin/abi` : `_pages/`);
        writeFileSync(outFile, banner + p.markdown);
    }

    // Manifest: groups → sections → items. Group order = first appearance.
    const groupOrder: string[] = [];
    for (const p of pages) if (!groupOrder.includes(p.group)) groupOrder.push(p.group);
    const manifest = {
        title: REFERENCE_TITLE,
        groups: groupOrder.map((gid) => {
            const gpages = pages.filter((p) => p.group === gid);
            const sectionIds: string[] = [];
            for (const p of gpages) if (!sectionIds.includes(p.section)) sectionIds.push(p.section);
            sectionIds.sort((a, b) => SECTION_ORDER.indexOf(a) - SECTION_ORDER.indexOf(b));
            return {
                id: gid,
                title: GROUP_TITLES[gid] ?? gid,
                sections: sectionIds.map((sid) => ({
                    id: sid,
                    title: SECTION_TITLES[sid] ?? sid,
                    items: gpages
                        .filter((p) => p.section === sid)
                        .map((p) => ({title: p.title, slug: p.slug, path: p.path, description: p.description})),
                })),
            };
        }),
    };
    mkdirSync(APP_DOCS_DIR, {recursive: true});
    writeFileSync(join(APP_DOCS_DIR, 'manifest.json'), JSON.stringify(manifest, null, 2) + '\n');

    // Pre-rendered HTML content module (imported by the app's /docs pages).
    const hl = await createHighlighter({
        themes: ['github-light', 'github-dark'],
        langs: ['solidity', 'typescript', 'javascript', 'bash', 'json', 'graphql'],
    });
    const content: Record<string, RenderedPage> = {};
    for (const p of pages) content[p.path.slice('/docs/'.length)] = renderPageHtml(hl, p);
    writeFileSync(join(APP_DOCS_DIR, 'content.json'), JSON.stringify(content) + '\n');

    // Search index.
    const search = pages.flatMap(indexPage);
    mkdirSync(APP_PUBLIC_DIR, {recursive: true});
    writeFileSync(join(APP_PUBLIC_DIR, 'docs-search-index.json'), JSON.stringify(search) + '\n');

    // Public ABIs (JSON copies of the checked-in @pin/abi exports).
    const publicAbiDir = join(APP_PUBLIC_DIR, 'abis');
    rmSync(publicAbiDir, {recursive: true, force: true});
    mkdirSync(publicAbiDir, {recursive: true});
    for (const [name, abi] of Object.entries(ABI_BY_NAME)) {
        writeFileSync(join(publicAbiDir, `${name}.json`), JSON.stringify(abi, null, 2) + '\n');
    }

    // Protocol manifest: contracts grouped by protocol.
    const protocolManifest = {
        name: 'PND onchain protocols',
        origin: SITE_ORIGIN,
        chainId: DEPLOYMENTS.chainId,
        docs: '/docs',
        llms: '/llms.txt',
        protocols: Object.fromEntries(
            PROTOCOLS.map((proto) => [
                proto.id,
                {
                    title: proto.title,
                    docs: `/docs/${proto.id}/overview`,
                    contracts: Object.fromEntries(
                        proto.contracts.map((c) => {
                            const built = pages.find((x) => x.group === proto.id && x.section === 'contracts' && x.slug === c.slug)!;
                            return [
                                c.name,
                                {
                                    address: resolveAddr(c.deploymentsKey),
                                    kind: c.kind,
                                    abi: `/abis/${c.name}.json`,
                                    docs: c.slug ? `/docs/${proto.id}/contracts/${c.slug}` : undefined,
                                    role: built.description,
                                },
                            ];
                        }),
                    ),
                },
            ]),
        ),
    };
    writeFileSync(join(APP_PUBLIC_DIR, 'protocol-manifest.json'), JSON.stringify(protocolManifest, null, 2) + '\n');

    // llms.txt
    const llms: string[] = [];
    llms.push('# PND onchain protocols');
    llms.push('');
    llms.push(
        '> The onchain protocols PND ships for artists. Each is a self-contained set of contracts with an API reference below. Collections are artist-owned ERC721 contracts built on a thin token core where every mint goes through an authorized minter, with a canonical fixed-price/referral minter wired by the factory. Auctions are per-owner English-auction houses for any ERC721. More protocols land here as they ship.',
    );
    llms.push('');
    llms.push(`Machine-readable addresses + ABIs: ${SITE_ORIGIN}/protocol-manifest.json (ABIs under ${SITE_ORIGIN}/abis/).`);
    llms.push('');
    for (const group of manifest.groups) {
        llms.push(`## ${group.title}`);
        llms.push('');
        for (const section of group.sections) {
            for (const item of section.items) {
                llms.push(`- [${item.title}](${SITE_ORIGIN}${item.path}): ${item.description}`);
            }
        }
        llms.push('');
    }
    writeFileSync(join(APP_PUBLIC_DIR, 'llms.txt'), llms.join('\n'));

    if (pendingKeys.size > 0) {
        console.log(`Pre-deploy: ${pendingKeys.size} address placeholder(s) unresolved (${[...pendingKeys].join(', ')}). Fill contracts/deployments.mainnet.json at launch.`);
    }
    const totalAccess = [...accessRows.values()].reduce((n, a) => n + a.length, 0);
    const totalErrors = [...errorRows.values()].reduce((n, a) => n + a.length, 0);
    const totalEvents = [...eventRows.values()].reduce((n, a) => n + a.length, 0);
    console.log(
        `Generated ${pages.length} pages across ${manifest.groups.length} groups, ${search.length} search entries, ${totalAccess} access rows, ${totalErrors} errors, ${totalEvents} events.`,
    );
}

main().catch((e: unknown) => {
    console.error(e instanceof Error ? e.message : e);
    process.exit(1);
});
